-- 四柱推命 해석문 테이블 + 시드.
--
-- 설계 의도:
--  · 짧은 라벨/키워드는 src/lib/saju/text.ts (정적, DB 왕복 0)
--  · 긴 해석문만 여기에 → 배포 없이 문구 수정 가능, AI 토큰 절약
--  · category+code 로 조회. AI가 못 붙어도(장애·상한) 이 텍스트만으로 화면이 성립한다.

create table if not exists saju_interpretations (
  id          bigserial primary key,
  category    text not null,          -- 'day_stem' | 'ten_god' | 'element_excess' | 'element_lack' | 'strength'
  code        text not null,          -- 甲 / 比肩 / 木 / strong ...
  lang        text not null default 'ja',
  title       text not null,
  body        text not null,
  created_at  timestamptz not null default now(),
  unique (category, code, lang)
);

create index if not exists idx_saju_interp_lookup
  on saju_interpretations (category, lang);

alter table saju_interpretations enable row level security;

-- 공개 읽기(익명 키로 조회 가능). 쓰기는 service_role만.
drop policy if exists "saju read" on saju_interpretations;
create policy "saju read" on saju_interpretations for select using (true);

