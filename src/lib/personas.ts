const BY_KEY = new Map(PERSONAS.map((p) => [p.key, p]));
export const DEFAULT_PERSONA: PersonaKey = 'hoshi';

/** 표시용 전체 이름(수식어 + 이름) */
export function fullName(p: Persona): string {
  return p.epithet ? `${p.epithet}${p.name}` : p.name;
}

/**
 * 임의의 값을 안전한 PersonaKey로 정규화.
 * 예전에 저장된 키(삭제된 'sakura' 'tsuki' 등)가 localStorage에 남아 있어도
 * 기본 캐릭터로 조용히 되돌아가므로 화면이 깨지지 않는다.
 */
export function toPersonaKey(v: unknown): PersonaKey {
  return typeof v === 'string' && BY_KEY.has(v as PersonaKey)
    ? (v as PersonaKey)
    : DEFAULT_PERSONA;
}

/** 서버에서 안전하게 페르소나를 해석(미지/누락이면 기본값). */
export function resolvePersona(key: unknown): Persona {
  return BY_KEY.get(toPersonaKey(key))!;
}

/**
 * system 프롬프트 앞에 붙일 캐릭터 블록.
 * 마지막 한 줄은 뒤에 이어질 "출력 형식 지시"까지 말투를 끌고 가기 위한 다리 역할.
 */
export function personaSystemPrefix(key: unknown): string {
  const p = resolvePersona(key);
  return `${p.prompt}\n${PERSONA_GUARD}\nこれから指定される出力形式のどの項目を書くときも、上の口調を最後まで保つこと。\n`;
}
