//
//   1) 문장의 길이와 리듬  (クロ는 짧게 끊고, じいさん은 사이를 둔다)
//   2) 어휘의 결          (한자어를 쓰는가, 순우리말스러운 말을 쓰는가)
//   3) 말을 꺼내는 순서    (결론부터인가, 공감부터인가)
//
// 그래서 각 캐릭터에 위 세 가지를 명시하고, ✅나쁜 예/좋은 예를 함께 준다.
// 예시(few-shot)는 말투를 잡는 데 가장 효과가 큰 방법이다.
//
// ─────────────────────────────────────────────────────────────
// 비용 메모
// ─────────────────────────────────────────────────────────────
// 프롬프트가 길어지지만 system 블록은 ephemeral 캐시에 올라가므로
// 같은 캐릭터를 다시 쓰면 입력 토큰이 거의 들지 않는다.
// 캐릭터 수 × 테마 수만큼 캐시 칸이 늘어나므로 4명이 적정선이다.

export type PersonaKey = 'hoshi' | 'kuro' | 'ojii' | 'koma';

export type Persona = {
  key: PersonaKey;
  /** 표시명(짧은 이름) */
  name: string;
  /** 이름 앞 수식어 — 성격이 드러나게 */
  epithet: string;
  /** 성격을 한 단어로 (선택 UI 배지) */
  tone: string;
  /** 한 줄 소개 */
  tagline: string;
  emoji: string;
  color: string;
  /** LLM에 주입할 캐릭터 규정 */
  prompt: string;
};

